#!/usr/bin/env python3

# 1. Method with no arguments
def greet_programmer():
    print("Hello, programmer!")

# 2. Method with one argument
def greet(name):
    print(f"Hello, {name}!")

# 3. Method with default argument
def greet_with_default(name="programmer"):
    print(f"Hello, {name}!")

# 4. Method to add two numbers
def add(num1, num2):
    return num1 + num2

# 5. Method to halve a number
def halve(number):
    return number / 2
